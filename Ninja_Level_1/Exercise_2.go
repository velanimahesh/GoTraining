// Use Var keyword to store the values
// variables are the x, y and z and
// values are of type int, string and bool
// What types of values are this?

package main

import "fmt"

var  x int
var  y string
var  z bool

func main()  {
	fmt.Println("x = ", x)
	fmt.Println("y = ", y)
	fmt.Println("z = ", z)

	fmt.Println("This default values are called a ZERO value")
}
